  <h1>Politique relative aux cookies</h1>
  <p><em>Version 1.2 — Dernière mise à jour : 22 mai 2024</em></p>

  <p>Bienvenue sur la documentation légale de <strong>Axent</strong>. Cette page détaille l'utilisation des traceurs sur notre plateforme conformément aux régulations en vigueur.</p>

  <h2>I. CADRE ET DÉFINITIONS</h2>
  <p><span class="article-num">Article 1 : Préambule.</span> La présente politique s'applique à l'ensemble du site axet.fr et de ses sous-domaines.</p>
  <p><span class="article-num">Article 2 : Objet.</span> Elle définit les types de traceurs utilisés et les moyens mis à disposition de l'utilisateur pour les contrôler.</p>
  <p><span class="article-num">Article 3 : Définition d'un cookie.</span> Un cookie est un fichier de petite taille, généralement constitué de lettres et de chiffres, déposé sur le terminal.</p>
  <p><span class="article-num">Article 4 : Responsable du traitement.</span> Le traitement est opéré par Axent, en sa qualité d'éditeur de la solution.</p>
  <p><span class="article-num">Article 5 : Conformité légale.</span> Axent s'engage à respecter les lignes directrices de la CNIL et le règlement RGPD.</p>

  <h2>II. TYPOLOGIE DES TRACEURS UTILISÉS</h2>
  <p><span class="article-num">Article 6 : Cookies de session.</span> Traceurs temporaires actifs uniquement durant la durée de votre navigation.</p>
  <p><span class="article-num">Article 7 : Cookies permanents.</span> Traceurs stockés sur le disque dur jusqu'à leur date d'expiration.</p>
  <p><span class="article-num">Article 8 : Cookies internes.</span> Déposés par le domaine axet.fr pour assurer les fonctionnalités natives.</p>
  <p><span class="article-num">Article 9 : Cookies tiers.</span> Déposés par des domaines partenaires pour des services externes.</p>
  <p><span class="article-num">Article 10 : Cookies strictement nécessaires.</span> Indispensables pour naviguer sur le site et utiliser ses fonctions sécurisées.</p>
  <p><span class="article-num">Article 11 : Cookies de performance.</span> Utilisés pour optimiser la vitesse de chargement des pages.</p>
  <p><span class="article-num">Article 12 : Cookies de mesure d'audience.</span> Permettent d'analyser le volume et l'origine du trafic de manière agrégée.</p>
  <p><span class="article-num">Article 13 : Cookies de personnalisation.</span> Mémorisent vos préférences telles que la langue ou le mode sombre.</p>
  <p><span class="article-num">Article 14 : Cookies publicitaires.</span> Utilisés pour limiter le nombre d'affichages d'une même publicité.</p>
  <p><span class="article-num">Article 15 : Cookies de réseaux sociaux.</span> Permettent l'interaction avec les plugins sociaux (boutons "Partager").</p>

  <h2>III. TECHNOLOGIES ET STOCKAGE</h2>
  <p><span class="article-num">Article 16 : Pixels invisibles.</span> Codes insérés pour suivre l'efficacité de nos campagnes de communication.</p>
  <p><span class="article-num">Article 17 : LocalStorage.</span> Stockage local persistant utilisé pour sauvegarder l'état du widget Axent.</p>
  <p><span class="article-num">Article 18 : SessionStorage.</span> Stockage temporaire pour maintenir l'intégrité des formulaires multi-étapes.</p>
  <p><span class="article-num">Article 19 : Empreinte numérique (Fingerprinting).</span> Nous n'utilisons pas de technique de fingerprinting pour identifier les individus.</p>
  <p><span class="article-num">Article 20 : Balises Web.</span> Utilisées dans nos emails transactionnels pour assurer la délivrabilité.</p>

  <h2>IV. GESTION ET CONSENTEMENT</h2>
  <p><span class="article-num">Article 21 : Recueil du consentement.</span> S'effectue via un bandeau explicite dès votre première connexion.</p>
  <p><span class="article-num">Article 22 : Preuve de consentement.</span> Chaque accord est horodaté et stocké de manière chiffrée via Axent.</p>
  <p><span class="article-num">Article 23 : Durée du consentement.</span> Votre choix est mémorisé pour une période glissante de 13 mois.</p>
  <p><span class="article-num">Article 24 : Retrait facilité.</span> Le retrait du consentement est aussi simple que son octroi via le widget permanent.</p>
  <p><span class="article-num">Article 25 : Refus par défaut.</span> Aucun cookie non-essentiel n'est déposé avant votre action positive.</p>

  <h2>V. PARAMÉTRAGE DES NAVIGATEURS</h2>
  <p><span class="article-num">Article 26 : Procédure Google Chrome.</span> Menu > Paramètres > Confidentialité et sécurité > Cookies.</p>
  <p><span class="article-num">Article 27 : Procédure Mozilla Firefox.</span> Menu > Options > Vie privée et sécurité > Cookies.</p>
  <p><span class="article-num">Article 28 : Procédure Apple Safari.</span> Préférences > Confidentialité > Cookies et données de site.</p>
  <p><span class="article-num">Article 29 : Procédure Microsoft Edge.</span> Paramètres > Cookies et autorisations de site.</p>
  <p><span class="article-num">Article 30 : Conséquences du blocage.</span> Le refus des cookies techniques peut altérer l'accès aux services authentifiés.</p>

  <h2>VI. PROTECTION DES DONNÉES</h2>
  <p><span class="article-num">Article 31 : Anonymisation de l'IP.</span> Les adresses IP sont hachées avant stockage dans nos journaux de consentement.</p>
  <p><span class="article-num">Article 32 : Droit d'accès.</span> Vous pouvez demander l'export des données de consentement vous concernant.</p>
  <p><span class="article-num">Article 33 : Droit à l'oubli.</span> La suppression des cookies sur votre terminal entraîne l'oubli local de vos choix.</p>
  <p><span class="article-num">Article 34 : Transferts hors Union Européenne.</span> Aucun transfert n'est opéré sans garanties contractuelles appropriées.</p>
  <p><span class="article-num">Article 35 : Sécurité des flux.</span> Toutes les données de cookies transitent via le protocole sécurisé HTTPS (TLS 1.3).</p>

  <h2>VII. DISPOSITIONS FINALES</h2>
  <p><span class="article-num">Article 36 : Mise à jour de la politique.</span> Axent se réserve le droit de modifier ce document à tout moment.</p>
  <p><span class="article-num">Article 37 : Notification de changement.</span> En cas de modification majeure, un nouveau consentement sera sollicité.</p>
  <p><span class="article-num">Article 38 : Contact DPO.</span> Pour toute question relative aux cookies : dpo@axet.fr.</p>
  <p><span class="article-num">Article 39 : Médiation.</span> En cas de litige, vous pouvez saisir la CNIL sur leur site officiel cnil.fr.</p>
  <p><span class="article-num">Article 40 : Langue officielle.</span> La version française de cette politique fait foi en cas de divergence de traduction.</p>

