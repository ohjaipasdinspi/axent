  <h1>Conditions Générales d'Utilisation (CGU)</h1>
  <p><em>Version 1.0 — Dernière mise à jour : 22 mai 2024</em></p>

  <span class="section-title">I. Mentions Légales et Définitions</span>
  <p><span class="article-num">Article 1 : Objet.</span> Les présentes CGU ont pour objet d'encadrer juridiquement les modalités de mise à disposition du site et des services Axent.</p>
  <p><span class="article-num">Article 2 : Éditeur.</span> Le site axet.fr est édité par l'équipe Axent. Contact : contact@axet.fr.</p>
  <p><span class="article-num">Article 3 : Utilisateur.</span> Est désigné comme Utilisateur toute personne physique ou morale accédant au site ou utilisant le SDK Axent.</p>
  <p><span class="article-num">Article 4 : Services.</span> Désigne l'ensemble des fonctionnalités de gestion de consentement proposées par Axent.</p>
  <p><span class="article-num">Article 5 : Plateforme.</span> Désigne l'interface technique accessible via les sous-domaines de axet.fr (app, admin, auth, api, cdn).</p>

  <span class="section-title">II. Accès et Inscription</span>
  <p><span class="article-num">Article 6 : Capacité juridique.</span> L'utilisation des services est réservée aux personnes majeures ou mineures avec autorisation parentale.</p>
  <p><span class="article-num">Article 7 : Création de compte.</span> L'accès au dashboard nécessite la création d'un compte avec une adresse email valide.</p>
  <p><span class="article-num">Article 8 : Exactitude des informations.</span> L'Utilisateur s'engage à fournir des informations réelles et à jour lors de son inscription.</p>
  <p><span class="article-num">Article 9 : Identifiants.</span> L'email et le mot de passe sont strictement personnels et confidentiels.</p>
  <p><span class="article-num">Article 10 : Sécurité du compte.</span> L'Utilisateur est seul responsable de toute activité effectuée avec ses identifiants.</p>

  <span class="section-title">III. Utilisation des Services</span>
  <p><span class="article-num">Article 11 : Installation du SDK.</span> Le SDK doit être intégré selon les instructions fournies dans la documentation officielle.</p>
  <p><span class="article-num">Article 12 : Usage Loyal.</span> L'Utilisateur s'interdit toute utilisation du service à des fins illégales ou malveillantes.</p>
  <p><span class="article-num">Article 13 : Limites techniques.</span> Axent se réserve le droit de limiter le nombre de requêtes API pour garantir la stabilité du serveur.</p>
  <p><span class="article-num">Article 14 : Personnalisation du Widget.</span> L'Utilisateur est responsable des textes et couleurs affichés via le widget sur son propre site.</p>
  <p><span class="article-num">Article 15 : Disponibilité.</span> Axent s'efforce d'assurer un accès 24h/24 mais ne garantit pas l'absence d'interruptions techniques.</p>

  <span class="section-title">IV. Propriété Intellectuelle</span>
  <p><span class="article-num">Article 16 : Propriété de l'Éditeur.</span> La structure, les codes sources, logos et graphismes sont la propriété exclusive d'Axent.</p>
  <p><span class="article-num">Article 17 : Licence d'utilisation.</span> Axent concède une licence limitée, non-exclusive et révocable pour l'usage du SDK.</p>
  <p><span class="article-num">Article 18 : Interdiction de Reverse-Engineering.</span> Toute décompilation ou tentative d'extraction du code source est interdite.</p>
  <p><span class="article-num">Article 19 : Marques.</span> Le nom "Axent" et son logo sont protégés. Leur usage sans accord écrit est proscrit.</p>
  <p><span class="article-num">Article 20 : Contenus Utilisateurs.</span> L'Utilisateur reste propriétaire des données de configuration qu'il saisit dans l'interface.</p>

  <span class="section-title">V. Responsabilités et Garanties</span>
  <p><span class="article-num">Article 21 : Absence de Garantie.</span> Le service est fourni "en l'état" sans garantie de performance spécifique.</p>
  <p><span class="article-num">Article 22 : Responsabilité Juridique.</span> L'Utilisateur est seul responsable de la conformité de son site avec les lois locales (CNIL, etc.).</p>
  <p><span class="article-num">Article 23 : Dommages Indirects.</span> Axent ne pourra être tenu responsable des pertes de revenus ou de données de l'Utilisateur.</p>
  <p><span class="article-num">Article 24 : Hébergement tiers.</span> Axent n'est pas responsable des pannes liées à l'hébergeur du serveur (ex: Plesk/Ovh).</p>
  <p><span class="article-num">Article 25 : Force Majeure.</span> La responsabilité d'Axent ne peut être engagée en cas de force majeure telle que définie par la jurisprudence.</p>

  <span class="section-title">VI. Données Personnelles (RGPD)</span>
  <p><span class="article-num">Article 26 : Collecte de données.</span> Axent collecte uniquement les données nécessaires au fonctionnement du service.</p>
  <p><span class="article-num">Article 27 : Finalité.</span> Les données servent à la gestion des comptes et à l'enregistrement des preuves de consentement.</p>
  <p><span class="article-num">Article 28 : Droit d'accès.</span> L'Utilisateur peut exercer ses droits d'accès et de rectification via la page dédiée.</p>
  <p><span class="article-num">Article 29 : Durée de conservation.</span> Les données de compte sont conservées tant que le compte est actif.</p>
  <p><span class="article-num">Article 30 : Sous-traitance.</span> L'Utilisateur accepte que les données de consentement soient stockées sur les serveurs d'Axent.</p>

  <span class="section-title">VII. Modification et Résiliation</span>
  <p><span class="article-num">Article 31 : Modification des CGU.</span> Axent se réserve le droit de modifier les présentes à tout moment.</p>
  <p><span class="article-num">Article 32 : Acceptation des modifications.</span> L'utilisation continue des services après modification vaut acceptation.</p>
  <p><span class="article-num">Article 33 : Résiliation par l'Utilisateur.</span> L'Utilisateur peut supprimer son compte à tout moment via son interface.</p>
  <p><span class="article-num">Article 34 : Suspension de compte.</span> En cas de violation des CGU, Axent peut suspendre l'accès sans préavis.</p>
  <p><span class="article-num">Article 35 : Effet de la résiliation.</span> La résiliation entraîne la suppression des clés API et l'arrêt du SDK sur les sites liés.</p>

  <span class="section-title">VIII. Droit Applicable et Juridiction</span>
  <p><span class="article-num">Article 36 : Loi Applicable.</span> Les présentes CGU sont soumises au droit français.</p>
  <p><span class="article-num">Article 37 : Règlement des litiges.</span> En cas de contestation, une solution amiable sera recherchée avant toute action judiciaire.</p>
  <p><span class="article-num">Article 38 : Tribunal Compétent.</span> Tout litige sera porté devant les tribunaux compétents du siège social de l'éditeur.</p>
  <p><span class="article-num">Article 39 : Divisibilité.</span> Si un article est jugé nul, les autres articles conservent leur pleine vigueur.</p>
  <p><span class="article-num">Article 40 : Langue.</span> En cas de traduction, seule la version française des CGU fait foi entre les parties.</p>

